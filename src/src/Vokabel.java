public class Vokabel {

    String f;
    String u;
    String a;

    public Vokabel(String f, String u) {
        this.f = f;
        this.u = u;
    }

    public Vokabel(String f, String u, String a) {
        this.f = f;
        this.u = u;
        this.a = a;
    }

    public String getF() {
        return f;
    }
    public String getU() {
        return u;
    }

    public String getA() {
        return a;
    }
}
